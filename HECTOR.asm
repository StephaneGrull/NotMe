    LD A, (activePlayer)    ; Detect active player
    CP 1                    ; if player 1
    JR Z, nextPlayerLost    ; next
    INC HL                  ; if player 2, increase adresse score (player 2)
    nextPlayerLost:
        LD A,(HL)
        CP 0x30
        JR Z,endPlayerLost
        LD A, 1                 ; Player not lost
        RET
